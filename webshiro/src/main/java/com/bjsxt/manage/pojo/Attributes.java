package com.bjsxt.manage.pojo;

public class Attributes {
	private String filename;
	public Attributes() {
		super();
	}

	public Attributes(String filename) {
		super();
		this.filename = filename;
	}

	public String getFilename() {
		return filename;
	}

	public void setFilename(String filename) {
		this.filename = filename;
	}
}
